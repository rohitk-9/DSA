def delNode(head, k):
    if k==1: return head.next
    curr=head
    count=1
    prev=None
    while count<k:
        prev=curr
        curr=curr.next
        count+=1
    if curr.next==None:
        prev.next=None
        return head
    prev.next=curr.next
    return head
